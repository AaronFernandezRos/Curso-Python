### Operadores Aritméticos ###

# Operaciones de enteros
# Suma de dos numeros
numero1 = 2
numero2 = 5
print("Suma de dos números")
print(numero1+numero2)

# RESTA
print("Resta de dos números")
print(numero1-numero2)

#Multiplicacion
print("Multiplicación de dos números")
print(numero1*numero2)

#Division
print("División de dos números")
print(numero1/numero2)

#Modulo (RESTO)
print("Resto de dos números")
print(numero1%numero2)

#División Entera (SIN DECIMALES)
print("Division sin decimales de dos números")
print(numero1//numero2)

#EXPONENTE
print("Exponente de dos números")
print(numero1**numero2)

#COMBINAR OPERACIONES
print("Exponente, resta, division y división entera de dos números")
print(2**3-7/1//4)

